// Fonction simple pour g√©n√©rer le CSS d'un th√®me
export function generateSiteThemeCSS(themeId, isDark = false) {
    const theme = SITE_THEMES[themeId] || SITE_THEMES.default
    const variables = isDark ? theme.cssVariables.dark : theme.cssVariables.light
    return variables
}

// Fonction pour obtenir la liste des th√®mes pour l'UI
export function getThemesList() {
    return Object.entries(SITE_THEMES).map(([id, theme]) => ({
        id,
        name: theme.name,
        description: theme.description,
        preview: theme.preview,
    }))
}
